App::uses('Model', 'Model');

/**
 * Application model for Cake.
 *
 * Add your application-wide methods in the class below, your models
 * will inherit them.
 *
 * @package       app.Model
 */
class AppModel extends Model {

    public $actsAs = array('Common');

    public function afterFind($results, $primary = false) {
        if (isset($results[0]['Cusine']) && count($results[0]['Cusine']) > 0) {
            $results = $this->createCusineURL($results);
            return $results;
        } elseif (isset($results[0]['Venue']) && count($results[0]['Venue']) > 0) {
            $results = $this->createVenueURL($results);
            return $results;
        }
    }

}
